# –°–æ–∑–¥–∞–Ω–∏–µ issue
create_issue() {
    local title="$1"
    local body="$2"
    local labels="$3"
    
    echo -e "${BLUE}üìù –°–û–ó–î–ê–ù–ò–ï ISSUE: $title${NC}"
    
    API_URL="https://api.github.com/repos/$GITHUB_USERNAME/moderation-system/issues"
    
    # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º JSON
    JSON_DATA=$(cat << EOF
{
  "title": "$title",
  "body": "$body",
  "labels": [$labels]
}
EOF
)
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å
    RESPONSE=$(curl -s -X POST \
      -H "Authorization: token $GITHUB_TOKEN" \
      -H "Accept: application/vnd.github.v3+json" \
      -H "Content-Type: application/json" \
      -d "$JSON_DATA" \
      "$API_URL")
    
    if echo "$RESPONSE" | grep -q '"number"'; then
        ISSUE_NUMBER=$(echo "$RESPONSE" | grep '"number"' | head -1 | cut -d':' -f2 | tr -d ' ,')
        ISSUE_URL=$(echo "$RESPONSE" | grep '"html_url"' | head -1 | cut -d'"' -f4)
        
        echo -e "${GREEN}‚úÖ Issue —Å–æ–∑–¥–∞–Ω!${NC}"
        echo "   –ù–æ–º–µ—Ä: #$ISSUE_NUMBER"
        echo "   URL: $ISSUE_URL"
        echo ""
        
        return "$ISSUE_NUMBER"
    else
        ERROR_MSG=$(echo "$RESPONSE" | grep -o '"message":"[^"]*"' | head -1)
        echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ issue: $ERROR_MSG${NC}"
